    private void calculerIntermediaire(java.awt.event.ActionEvent evt) {
        if (!txtAffiche.getText().isEmpty()) {
            String[] parties = txtAffiche.getText().split(" ");
            secondOperande = Double.parseDouble(parties[2].trim());
            premierOperande = calculer();
            txtAffiche.setText(String.valueOf(premierOperande));
        }
    }
    
    private double calculer() {
        switch (operation) {
            case '+':
                return premierOperande + secondOperande;
            case '-':
                return premierOperande - secondOperande;
            case '*':
                return premierOperande * secondOperande;
            case '/':
                if (secondOperande != 0) {
                    return premierOperande / secondOperande;
                } else {
                    txtAffiche.setText("Erreur");
                    return 0;
                }
            default:
                return secondOperande;
        }
    }
    
    private void ajouterChiffre(String chiffre) {
        if (attendNouvelOperande || txtAffiche.getText().equals("0")) {
            txtAffiche.setText(chiffre);
            attendNouvelOperande = false;
        } else {
            txtAffiche.setText(txtAffiche.getText() + chiffre);
        }
    }
